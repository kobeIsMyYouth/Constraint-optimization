def trasfer(A, b):
    """
    将分布式语言偏好关系矩阵转化为数值型偏好关系
    :param A: 分布式语言偏好关系矩阵：A=[[[("好", 0.1), (), (), ... ()],[(), (), (), ... ()],...[(), (), (), ... ()]],
                                        [[(), (), (), ... ()],[(), (), (), ... ()],...[(), (), (), ... ()]],
                                        [[(), (), (), ... ()],[(), (), (), ... ()],...[(), (), (), ... ()]],
                                        [[(), (), (), ... ()],[(), (), (), ... ()],...[(), (), (), ... ()]]]
    :param b: 数值化的分布式语言偏好关系得分值
    :return: 数值型偏好关系矩阵：B=[[1, 2, 3, 4, 5, 6, 7, 8, 9],
                                    [1, 2, 3, 4, 5, 6, 7, 8, 9],
                                    [1, 2, 3, 4, 5, 6, 7, 8, 9]]
    """
    B = np.zeros((A.shape[0], A.shape[1]))
    for i in range(A.shape[0]):
        for j in range(A.shape[1]):
            sum = 0
            for k in range(len(b)):
                sum = b[k] * float(A[i, j, k, 1]) + sum
            B[i, j] = sum
    return B

